    public void datosConsultados(String tipoUser, String nombreUser) {
    try {
            Conexion conex = new Conexion();
            conex.conexionBd();
            PreparedStatement pst = conex.conexionBd().prepareStatement("SELECT marca from equipos" );
            
            ResultSet rs = pst.executeQuery();
         
          
                while(rs.next()){
                marca = rs.getString("marca"); 
             
           if(marca.equals("Acer"))
               acer++;
               
            else if(marca.equals("Apple"))
                apple++;
            else if (marca.equals("Asus"))
                asus++;
            else if (marca.equals("Alienware"))
                alienware++;
            else if (marca.equals("Brother"))
                brother++;
            else if (marca.equals("Dell"))
                dell++;
            else if (marca.equals("Hp"))
                hp++;
            else if (marca.equals("Janus"))
                janus++;
            else if (marca.equals("Lenovo"))
                lenovo++;
            else if (marca.equals("Samsung"))
                samsung++;
            else if (marca.equals("Toshiba"))
                toshiba++;
            else if (marca.equals("Xerox"))
                xerox++;
            else
                otra++;    
                      
            }
            
            conex.conexionBd().close();
            rs.close();
            pst.getConnection().close();
            
            initComponents();
            this.setLocationRelativeTo(null);
                    
        } catch (SQLException e) {
            System.out.println("ERROR en la conexion a la BD" + e);
        }
    }
