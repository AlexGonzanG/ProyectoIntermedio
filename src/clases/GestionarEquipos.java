     public void mostrarDatos() {
            try {
            Conexion conex = new Conexion();
            conex.conexionBd();
            PreparedStatement pst = conex.conexionBd().prepareStatement("select id_equipo, tipo_equipo, marca, status from equipos");

            ResultSet rs = pst.executeQuery();
            

              while (rs.next()) {

                agregar[0] = rs.getString("id_equipo");
                agregar[1] = rs.getString("tipo_equipo");
                agregar[2] = rs.getString("marca");
                agregar[3] = rs.getString("status");
                model.addRow(agregar);

            }
            conex.conexionBd().close();
            rs.close();
            pst.getConnection().close();
        } catch (SQLException e) {
            System.out.println("ERROR en la conexion a la BD" + e);
        }

    }
    
    
    
