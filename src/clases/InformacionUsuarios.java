    public void datosConsultados(String idUsuario, String usernameUsuario) {
        this.idUsuario = Integer.parseInt(idUsuario);
        this.usernameUsuario = usernameUsuario;
        

        try {
            Conexion conex = new Conexion();
            conex.conexionBd();
            PreparedStatement pst;
            pst = conex.conexionBd().prepareStatement("select * from usuarios where id_usuario = ?");
            pst.setInt(1, this.idUsuario);

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txt_nombre.setText(rs.getString("nombre_usuario"));
                txt_email.setText(rs.getString("email"));
                txt_telefono.setText(rs.getString("telefono"));
                txt_username.setText(rs.getString("username"));
                pass = rs.getString("password");
                jComboBox1.setSelectedItem(rs.getString("status"));
                jComboBox2.setSelectedItem(rs.getString("tipo_nivel"));
                txt_registradoPor.setText(rs.getString("registrado_por"));
                this.setTitle("Información usuario " + this.usernameUsuario + " Sesion de " + Login.tipoUser + " " + Login.username);
                jLabel1.setText("Informacion del usuario " + this.usernameUsuario);
                
            }
            
            conex.conexionBd().close();
            rs.close();
            pst.getConnection().close();

        } catch (SQLException e) {
            System.out.println("ERROR en la conexion a la BD" + e);
        }

    }

