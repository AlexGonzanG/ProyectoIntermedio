    private void BotonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonActualizarActionPerformed
        try {
            Conexion conex = new Conexion();
            conex.conexionBd();
            PreparedStatement pst = conex.conexionBd().prepareStatement("update usuarios SET id_usuario=?, "
                    + "nombre_usuario=?, email=?, telefono=?, username=?, password=?, tipo_nivel=?, status=?, "
                    + "registrado_por=? where id_usuario = ?");

            pst.setInt(1, idUsuario);
            pst.setString(2, txt_nombre.getText().trim());
            pst.setString(3, txt_email.getText().trim());
            pst.setString(4, txt_telefono.getText().trim());
            pst.setString(5, txt_username.getText().trim());
            pst.setString(6, pass);
            pst.setString(7, jComboBox2.getSelectedItem().toString());
            pst.setString(8, jComboBox1.getSelectedItem().toString());
            pst.setString(9, txt_registradoPor.getText().trim());
            pst.setInt(10, idUsuario);
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Actualizaciòn Exitosa");
            this.setVisible(false);
            conex.conexionBd().close();
            pst.getConnection().close();
            
        } catch (SQLException e) {
            System.out.println("ERROR en la conexion a la BD" + e);
        }
    }//GEN-LAST:event_BotonActualizarActionPerformed

